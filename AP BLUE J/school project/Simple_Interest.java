class Simple_Interest
{
    public static void main(String args[])/**main function definition*/
    {
        double P=5000.0,R=5.75,T=3.0,SI=0.0;/**variable declaration and initialization*/
        SI=(P*R*T)*0.01;/**calculation of Simple Interest*/
        System.out.println("The Simple Interest of Rs. "+P+" at the rate of "+R+"% for "+T+"yrs is="+SI);/**displaying the Simple Interest*/
    }/**end of main method*/
}/**end of class*/
