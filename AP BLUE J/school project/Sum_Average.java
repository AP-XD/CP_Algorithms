class Sum_Average
{
    public static void main(String args[])/**main function definition*/
    {
        int a=5,b=10,c=15,d=20,e=30,sum=0; double avg=0.0;/**variable declaration and initialization*/
        sum=a+b+c+d+e;/**calculation of addition of 5 numbers*/
        avg=sum/5.0;/**calculation of average of 5 numbers*/
        System.out.println("The sum of "+a+" and "+b+" and "+c+" and "+d+" and "+e+" is="+sum);/**displaying the sum of five numbers*/
        System.out.println("The average of "+a+" and "+b+" and "+c+" and "+d+" and "+e+" is="+avg);/**displaying the average of five numbers*/
        
    }/**end of main method*/
}/**end of class*/
